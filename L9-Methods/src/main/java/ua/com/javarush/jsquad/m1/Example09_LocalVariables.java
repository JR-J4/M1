package ua.com.javarush.jsquad.m1;

/**
 * Лекція 9: Методи — Локальні змінні
 *
 * Будь-яка змінна, оголошена всередині методу або блоку, живе лише в межах цього блоку.
 * За його межами змінна недоступна і збирається збирачем сміття.
 *
 * Аналогія: аркуш нотаток на нараді — після зустрічі ви його викидаєте, інші команди про нього не знають.
 * Реальне застосування: тимчасові змінні розрахунків, лічильники циклів, прапорці умов.
 */
public class Example09_LocalVariables {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Змінні всередині умовного блоку
        // ============================================================
        System.out.println("=== Локальні змінні в if ===");
        String ticketStatus = "NEW";
        if ("NEW".equals(ticketStatus)) {
            String assignee = "Марія";
            System.out.println("Призначаємо заявку менеджеру " + assignee);
        }
        // System.out.println(assignee); // так робити не можна — змінна поза блоком

        System.out.println();

        // ============================================================
        //   Приклад 2: Локальні змінні циклів
        // ============================================================
        System.out.println("=== Локальні змінні циклу for ===");
        for (int i = 1; i <= 3; i++) {
            String step = "Крок " + i;
            System.out.println(step);
        }
        // i та step існували лише під час виконання циклу

        System.out.println();

        // ============================================================
        //   Приклад 3: Вкладені блоки коду
        // ============================================================
        System.out.println("=== Вкладені блоки ===");
        summarizeMeeting();
    }

    private static void summarizeMeeting() {
        int participants = 6;
        System.out.println("Учасників: " + participants);
        {
            String decision = "Запускаємо пілот у травні";
            System.out.println("Рішення: " + decision);
        }
        // decision тут вже недоступна, але можна створити нову змінну з тим самим ім'ям
        String decision = "Фінальний реліз у липні";
        System.out.println("Оновлене рішення: " + decision);
    }
}
