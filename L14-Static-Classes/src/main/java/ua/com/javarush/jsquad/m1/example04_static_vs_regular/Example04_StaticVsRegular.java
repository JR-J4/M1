package ua.com.javarush.jsquad.m1.example04_static_vs_regular;

/**
 * Лекція 14: Класи та static
 * <hr>
 * <h3>Тема: Статичні vs звичайні методи — порівняння</h3>
 *
 * <p>Статичні методи прив'язані до класу і викликаються через ім'я класу.
 * Звичайні методи прив'язані до об'єкта і потребують створення екземпляра.</p>
 *
 * <p><b>Таблиця порівняння:</b></p>
 * <pre>
 *   Здатність                              | Звичайний | Статичний
 *   ─────────────────────────────────────────┼───────────┼──────────
 *   Є зв'язок з екземпляром класу           |    Так    |    Ні
 *   Може викликати звичайні методи класу     |    Так    |    Ні
 *   Може викликати статичні методи класу     |    Так    |    Так
 *   Може звертатися до звичайних змінних     |    Так    |    Ні
 *   Може звертатися до статичних змінних     |    Так    |    Так
 *   Може бути викликаний у об'єкта           |    Так    |    Так
 *   Може бути викликаний у класу             |    Ні     |    Так
 * </pre>
 *
 * <p><b>Аналогія з життя:</b>
 * Банк: курс валюти — один для всіх клієнтів (статичний метод),
 * а баланс рахунку — у кожного свій (звичайний метод).</p>
 *
 * <p><b>Реальне застосування:</b>
 * Статичні — для утиліт, фабрик, конвертерів. Звичайні — для бізнес-логіки
 * конкретних об'єктів (рахунки, замовлення, користувачі).</p>
 */
public class Example04_StaticVsRegular {

    public static void main(String[] args) {

        // === 1. Статичний метод — виклик через ім'я класу ===
        // Сценарій: банк — перевіряємо курс валюти (не потрібен рахунок)

        System.out.println("=== Статичний метод — без створення об'єкта ===");

        // Не потрібно створювати BankAccount, щоб дізнатися курс
        double rate = BankAccount.getExchangeRate();
        System.out.println("Поточний курс USD: " + rate + " грн");

        double usd = BankAccount.convertToUSD(10000);
        System.out.println("10000 грн = " + String.format("%.2f", usd) + " USD");



        System.out.println();

        // === 2. Звичайний метод — потрібен об'єкт ===
        // Щоб поповнити рахунок або зняти кошти — потрібен конкретний рахунок

        System.out.println("=== Звичайний метод — потрібен об'єкт ===");

        BankAccount aliceAccount = new BankAccount("Аліса", 5000);
        BankAccount bobAccount = new BankAccount("Боб", 12000);


        double exchangeRate = BankAccount.getExchangeRate();

        aliceAccount.deposit(3000);    // потрібен конкретний рахунок
        bobAccount.withdraw(2000);     // потрібен конкретний рахунок

        // BankAccount.deposit(1000);  // ПОМИЛКА! Звичайний метод не можна
                                       // викликати через ім'я класу

        System.out.println();

        // === 3. Звичайний метод може використовувати статичні дані ===
        // Звичайний метод "бачить" все: і статичне, і нестатичне

        System.out.println("=== Звичайний метод використовує статичні дані ===");

        aliceAccount.showBalanceInUSD();  // звичайний метод звертається до статичного курсу
        bobAccount.showBalanceInUSD();

        // Змінимо курс (статичний метод)
        System.out.println();
        System.out.println("НБУ змінив курс...");
        BankAccount.setExchangeRate(42.00);

        // Зміна курсу впливає на ВСІ рахунки
        aliceAccount.showBalanceInUSD();
        bobAccount.showBalanceInUSD();

        System.out.println();

        // === 4. Підсумкова таблиця ===

        System.out.println("=== Порівняння: статичні vs звичайні методи ===");
        System.out.println();
        System.out.println("  Здатність                          | Звичайний | Статичний");
        System.out.println("  ───────────────────────────────────┼──────────┼──────────");
        System.out.println("  Є зв'язок з екземпляром            |   Так    |   Ні");
        System.out.println("  Може викликати звичайні методи     |   Так    |   Ні");
        System.out.println("  Може викликати статичні методи     |   Так    |   Так");
        System.out.println("  Може звертатися до звичайних полів |   Так    |   Ні");
        System.out.println("  Може звертатися до статичних полів |   Так    |   Так");
        System.out.println("  Викликається через клас            |   Ні     |   Так");
        System.out.println("  Викликається через об'єкт          |   Так    |   Так");
    }
}
