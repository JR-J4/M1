package ua.com.javarush.jsquad.m1.example06_initialization_order;

/**
 * Лекція 14: Класи та static
 * <hr>
 * <h3>Тема: Порядок ініціалізації</h3>
 *
 * <p>Коли створюється об'єкт класу, Java виконує ініціалізацію у суворому порядку:</p>
 * <pre>
 *   1. Статичні блоки та поля ЗОВНІШНЬОГО класу (в порядку розміщення у класі)
 *   2. Статичні блоки та поля ВНУТРІШНЬОГО класу (в порядку розміщення у класі)
 *   3. Блоки ініціалізації та нестатичні поля ЗОВНІШНЬОГО класу
 *   4. Конструктор ЗОВНІШНЬОГО класу
 *   5. Блоки ініціалізації та нестатичні поля ВНУТРІШНЬОГО класу
 *   6. Конструктор ВНУТРІШНЬОГО класу
 * </pre>
 *
 * <p><b>Важливо:</b> статичні блоки виконуються лише ОДИН раз — при першому
 * завантаженні класу. Нестатичні блоки та конструктор — при кожному new.</p>
 *
 * <p><b>Аналогія з життя:</b>
 * Запуск додатку: спочатку завантажуються глобальні налаштування (static),
 * потім створюється вікно програми (екземпляр), і нарешті завантажуються
 * модулі всередині вікна (внутрішній клас).</p>
 *
 * <p><b>Реальне застосування:</b>
 * Розуміння порядку ініціалізації критичне для коректної роботи з
 * Singleton, фабриками, та будь-яким кодом, де важлива послідовність
 * створення об'єктів.</p>
 */
public class Example06_InitializationOrder {

    public static void main(String[] args) {

        // === 1. Статичні блоки та поля (виконуються при завантаженні класу) ===
        // Сценарій: запуск додатку — спостерігаємо порядок ініціалізації

        System.out.println("=== Створення першого об'єкта Application ===");
        System.out.println("(Зверніть увагу на порядок кроків!)");
        System.out.println();

        // При першому new Application():
        // Крок 1: статичні поля та блоки зовнішнього класу
        // Крок 3: нестатичні поля та блоки зовнішнього класу
        // Крок 4: конструктор зовнішнього класу
        Application app = new Application();

        System.out.println();

        // === 2. Блоки ініціалізації та конструктор (при кожному new) ===
        // Статичні блоки НЕ виконуються повторно!

        System.out.println("=== Створення другого об'єкта Application ===");
        System.out.println("(Статичні блоки вже НЕ виконуються!)");
        System.out.println();

        Application app2 = new Application();
        // Бачимо тільки кроки 3 та 4 — статичні (крок 1) вже відпрацювали

        System.out.println();

        // === 3. Повна демонстрація з внутрішнім класом ===
        // Кроки 5 та 6 виконуються при створенні внутрішнього класу

        System.out.println("=== Створення Application + Module (повний порядок) ===");
        System.out.println();

        Application app3 = new Application();          // кроки 3, 4
        Application.Module module = app3.new Module(); // кроки 5, 6

        System.out.println();
        module.showInfo();

        System.out.println();

        // === Підсумок ===

        System.out.println("=== Підсумок: порядок ініціалізації ===");
        System.out.println();
        System.out.println("  1. Статичні блоки/поля зовнішнього класу  (один раз!)");
        System.out.println("  2. Статичні блоки/поля внутрішнього класу (один раз!)");
        System.out.println("  3. Нестатичні блоки/поля зовнішнього класу");
        System.out.println("  4. Конструктор зовнішнього класу");
        System.out.println("  5. Нестатичні блоки/поля внутрішнього класу");
        System.out.println("  6. Конструктор внутрішнього класу");
        System.out.println();
        System.out.println("  Статичне = при завантаженні класу (один раз)");
        System.out.println("  Нестатичне = при кожному new");
    }
}
