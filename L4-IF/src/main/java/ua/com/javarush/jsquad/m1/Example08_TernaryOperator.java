package ua.com.javarush.jsquad.m1;

/**
 * Лекція 4: Тернарний оператор
 *
 * "Тернарний" означає "потрійний". Це скорочена форма if-else,
 * яка повертає значення (є виразом, а не командою).
 *
 * Синтаксис: умова ? вираз1 : вираз2
 *   - Якщо умова true -> повертає вираз1
 *   - Якщо умова false -> повертає вираз2
 *
 * Аналогія з життя: тернарний оператор — це як питання з двома відповідями.
 * "Йде дощ? Якщо так — бери парасольку, якщо ні — бери окуляри."
 * Ви ЗАВЖДИ отримуєте щось у результаті.
 *
 * Вирази та команди:
 *   Команда (Statement) — виконується:  int x;
 *   Вираз (Expression) — обчислюється і має результат:  (a < 10), i++, x = 5
 *
 * Реальне застосування: вибір значення, формування тексту, мінімум/максимум.
 */
public class Example08_TernaryOperator {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: if-else vs тернарний оператор
        // ============================================================
        System.out.println("=== if-else vs тернарний ===");

        int age = 25;

        // Спосіб 1: з if-else (4 рядки)
        int money1;
        if (age > 30) {
            money1 = 100;
        } else {
            money1 = 50;
        }

        // Спосіб 2: тернарний оператор (1 рядок!)
        int money2 = age > 30 ? 100 : 50;

        System.out.println("if-else:    money = " + money1);    // 50
        System.out.println("тернарний:  money = " + money2);    // 50
        System.out.println("Результат однаковий!");
        System.out.println();

        // ============================================================
        //   Приклад 2: Знаходження мінімуму
        // ============================================================
        System.out.println("=== Мінімум двох чисел ===");

        // Уявіть: порівняння цін у двох магазинах
        int priceA = 250;
        int priceB = 250;



        String bestPrice =  priceA < priceB ? "priceA" : "priceB";

        System.out.println("Магазин А: " + priceA + " грн");
        System.out.println("Магазин Б: " + priceB + " грн");
        System.out.println("Краща ціна: " + bestPrice + " грн");
        System.out.println();

        // ============================================================
        //   Приклад 3: Формування тексту
        // ============================================================
        System.out.println("=== Формування тексту ===");

        // Уявіть: показати статус замовлення
        boolean isPaid = true;
        String status = isPaid ? "Оплачено" : "Очікує оплати";
        System.out.println("Статус замовлення: " + status);

        // Уявіть: вітання залежно від часу доби
        int hour = 14;
        String greeting = hour < 12 ? "Доброго ранку!" : "Доброго дня!";
        System.out.println(greeting);
        System.out.println();

        // ============================================================
        //   Приклад 4: Парне/непарне одним рядком
        // ============================================================
        System.out.println("=== Парне/непарне ===");

        int number = 42;
        String result = number % 2 == 0 ? "парне" : "непарне";
        System.out.println(number + " — " + result + " число");
        // Результат: "42 — парне число"
    }
}
