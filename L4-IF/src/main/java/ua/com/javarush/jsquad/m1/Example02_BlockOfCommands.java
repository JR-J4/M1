package ua.com.javarush.jsquad.m1;

/**
 * Лекція 4: Блоки команд
 *
 * Блок команд — це кілька команд, об'єднаних у фігурних дужках { }.
 *
 * Аналогія з життя: Блок команд — це як список справ у конверті.
 * Ви відкриваєте конверт { і виконуєте всі справи зі списку,
 * потім закриваєте конверт }.
 *
 * Без дужок if виконає тільки ОДНУ наступну команду!
 * З дужками — виконає ВСЕ, що всередині блоку.
 *
 * Реальне застосування: коли треба зробити кілька дій за однієї умови.
 */
public class Example02_BlockOfCommands {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Блок команд з if-else
        // ============================================================
        System.out.println("=== Блок команд: температура ===");

        // Уявіть: ви вирішуєте, як одягнутись
        int temperature = 5;

        if (temperature < 0) {
            // Це блок команд — обидві виконаються разом
            System.out.println("На вулиці мороз");
            System.out.println("Шапку одягни!");
        } else {
            System.out.println("Тепло");
        }
        // Результат: "Тепло" (бо 5 >= 0)

        System.out.println();

        // ============================================================
        //   Приклад 2: Порожній блок else
        // ============================================================
        System.out.println("=== Порожній блок else ===");

        // Уявіть: повістка з військкомату — стосується тільки тих, кому 18
        int vikAge = 21;

        if (vikAge == 18) {
            System.out.println("З'явіться до військкомату");
        } else {
            // Порожній блок — нічого не робимо
            // На екран нічого не виведеться
        }

        System.out.println("(нічого не вивелось, бо вік = " + vikAge + ", а не 18)");
        System.out.println();

        // ============================================================
        //   Приклад 3: Кілька дій при покупці
        // ============================================================
        System.out.println("=== Покупка в магазині ===");

        // Уявіть: ви на касі, перевіряєте чи вистачає грошей
        int price = 150;
        int wallet = 200;

        if (wallet >= price) {
            System.out.println("Оплата пройшла!");
            System.out.println("Решта: " + (wallet - price) + " грн");
            System.out.println("Дякуємо за покупку!");
        } else {
            System.out.println("Недостатньо коштів!");
            System.out.println("Не вистачає: " + (price - wallet) + " грн");
        }
        // Результат: "Оплата пройшла!", "Решта: 50 грн", "Дякуємо за покупку!"
    }
}
