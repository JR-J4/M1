package ua.com.javarush.jsquad.m1;

/**
 * Лекція 4: Оператори порівняння
 *
 * У Java є 6 операторів для порівняння значень:
 *   <   — менше
 *   >   — більше
 *   <=  — менше або дорівнює
 *   >=  — більше або дорівнює
 *   ==  — дорівнює (два знаки рівності!)
 *   !=  — НЕ дорівнює
 *
 * Аналогія з життя: порівняння — це як ваги на базарі.
 * Ви кладете два предмети на чаші і дивитесь:
 * один важчий, легший, або вони рівні.
 *
 * УВАГА: == (порівняння) це НЕ = (присвоєння)!
 * a == b -> "чи a дорівнює b?" (питання, повертає true/false)
 * a = b  -> "запиши значення b в змінну a" (дія)
 *
 * Реальне застосування: перевірка паролів, фільтрація, пошук.
 */
public class Example06_ComparisonOperators {

    public static void main(String[] args) {

        // ============================================================
        //   Усі 6 операторів порівняння
        // ============================================================
        System.out.println("=== 6 операторів порівняння ===");

        int a = 10;
        int b = 20;

        System.out.println("a = " + a + ", b = " + b);
        System.out.println();
        System.out.println("a < b  -> " + (a < b));   // true  (10 менше за 20)
        System.out.println("a > b  -> " + (a > b));   // false (10 не більше за 20)
        System.out.println("a <= b -> " + (a <= b));   // true  (10 менше або дорівнює 20)
        System.out.println("a >= b -> " + (a >= b));   // false (10 не більше і не дорівнює 20)
        System.out.println("a == b -> " + (a == b));   // false (10 не дорівнює 20)
        System.out.println("a != b -> " + (a != b));   // true  (10 не дорівнює 20)
        System.out.println();

        // ============================================================
        //   Приклад: перевірка швидкості на дорозі
        // ============================================================
        System.out.println("=== Контроль швидкості ===");

        // Уявіть: камера на дорозі перевіряє швидкість авто
        int speed = 65;
        int maxSpeed = 60;

        if (speed > maxSpeed) {
            System.out.println("Перевищення швидкості! " + speed + " км/год > " + maxSpeed + " км/год");
            System.out.println("Штраф!");
        } else {
            System.out.println("Швидкість в нормі: " + speed + " км/год");
        }

        System.out.println();

        // ============================================================
        //   Приклад: перевірка пін-коду
        // ============================================================
        System.out.println("=== Перевірка пін-коду ===");

        // Уявіть: банкомат перевіряє ваш пін
        int correctPin = 1234;
        int enteredPin = 1234;

        if (enteredPin == correctPin) {
            System.out.println("Пін-код вірний. Доступ дозволено!");
        } else {
            System.out.println("Невірний пін-код. Спробуйте ще раз.");
        }
        System.out.println();

        // ============================================================
        //   Приклад: перевірка залишку на складі
        // ============================================================
        System.out.println("=== Залишок на складі ===");

        // Уявіть: інтернет-магазин перевіряє наявність товару
        int stock = -2;

        if (stock > 0) {
            System.out.println("Товар є в наявності: " + stock + " шт.");
        } else {
            System.out.println("Товару немає в наявності");
        }
    }
}
