package ua.com.javarush.jsquad.m1;

/**
 * Лекція 4: Скорочена форма оператора if (без else)
 *
 * Якщо команду потрібно виконати ТІЛЬКИ коли умова істинна,
 * а коли хибна — нічого не робити, можна не писати else.
 *
 * Аналогія з життя: Ви стоїте на зупинці і чекаєте автобус №62.
 * Якщо приїхав 62 — сідаєте. Якщо ні — просто стоїте далі.
 * Вам не треба "інакше — зроби щось". Просто нічого не робите.
 *
 * Синтаксис:
 *   if (умова) {
 *       команда1;
 *   }
 *   // далі програма продовжується
 *
 * Реальне застосування: повідомлення, логування, попередження.
 */
public class Example03_IfWithoutElse {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Автобус на зупинці
        // ============================================================
        System.out.println("=== Автобус на зупинці ===");

        // Уявіть: ви чекаєте на автобус №62
        int bus = 62;

        if (bus == 62) {
            System.out.println("Це наш автобус! Заходимо!");
        }
        // Якщо bus != 62, програма просто йде далі

        System.out.println();

        // ============================================================
        //   Приклад 2: Попередження про низький заряд батареї
        // ============================================================
        System.out.println("=== Заряд батареї ===");

        // Уявіть: телефон перевіряє рівень заряду
        int battery = 15;

        if (battery < 20) {
            System.out.println("Увага! Низький заряд батареї: " + battery + "%");
        }
        // Якщо заряд >= 20, нічого не виводимо — все ОК

        System.out.println();

        // ============================================================
        //   Приклад 3: Привітання з днем народження
        // ============================================================
        System.out.println("=== День народження ===");

        // Уявіть: додаток перевіряє чи сьогодні день народження
        int todayDay = 15;
        int todayMonth = 3;
        int birthdayDay = 15;
        int birthdayMonth = 3;

        if (todayDay == birthdayDay && todayMonth == birthdayMonth) {
            System.out.println("З Днем народження! Бажаємо щастя!");
        }
        // Якщо не день народження — програма мовчить

        System.out.println();

        // ============================================================
        //   Приклад 4: Знижка для пенсіонерів
        // ============================================================
        System.out.println("=== Знижка в магазині ===");

        int customerAge = 70;
        int price = 100;

        if (customerAge >= 65) {
            price = price - 10; // знижка 10 грн
            System.out.println("Вам надано знижку для пенсіонерів!");
        }

        System.out.println("Ціна до оплати: " + price + " грн");
    }
}
