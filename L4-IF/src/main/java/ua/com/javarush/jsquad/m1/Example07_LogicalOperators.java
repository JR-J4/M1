package ua.com.javarush.jsquad.m1;

/**
 * Лекція 4: Логічні оператори AND (&&), OR (||), NOT (!)
 *
 * Логічні оператори дозволяють комбінувати кілька умов:
 *   &&  (І / AND)  — обидві умови мають бути true
 *   ||  (АБО / OR) — хоча б одна умова має бути true
 *   !   (НЕ / NOT) — інвертує значення (true -> false, false -> true)
 *
 * Аналогія з життя:
 *   && (І): "Я піду на прогулянку, ЯКЩО тепло І не йде дощ"
 *           — потрібно щоб ОБИДВІ умови виконались.
 *
 *   || (АБО): "Я буду щасливий, ЯКЩО здам іспит АБО отримаю подарунок"
 *             — достатньо ОДНІЄЇ з умов.
 *
 *   ! (НЕ): "Я вийду на вулицю, ЯКЩО НЕ йде дощ"
 *           — перевертає умову навпаки.
 *
 * Реальне застосування: форми реєстрації, фільтри, правила доступу.
 */
public class Example07_LogicalOperators {

    public static void main(String[] args) {

        // ============================================================
        //   Оператор && (І / AND) — обидві умови true
        // ============================================================
        System.out.println("=== Оператор && (І) ===");

        // Уявіть: ви хочете взяти кредит у банку
        // Потрібно: вік >= 18 І зарплата >= 10000
        int age = 25;
        int salary = 15000;

        if (age >= 18 && salary >= 10000) {
            System.out.println("Кредит схвалено!");
        } else {
            System.out.println("Кредит відхилено.");
        }
        // Результат: "Кредит схвалено!" (25 >= 18 — true, 15000 >= 10000 — true)

        System.out.println();

        // Перевірка діапазону: число від 0 до 100
        int score = 85;
        if (score >= 0 && score <= 100) {
            System.out.println("Бал " + score + " — коректний (від 0 до 100)");
        }
        System.out.println();

        // ============================================================
        //   Оператор || (АБО / OR) — хоча б одна true
        // ============================================================
        System.out.println("=== Оператор || (АБО) ===");

        // Уявіть: знижка в магазині для студентів АБО пенсіонерів
        boolean isStudent = true;
        boolean isPensioner = false;

        if (isStudent || isPensioner) {
            System.out.println("Вам надано знижку 10%!");
        } else {
            System.out.println("Знижка не передбачена.");
        }
        // Результат: "Вам надано знижку 10%!" (студент = true — достатньо)

        System.out.println();

        // Уявіть: вихідний день — субота або неділя
        int dayOfWeek = 6; // 6 = субота
        if (dayOfWeek == 6 || dayOfWeek == 7) {
            System.out.println("Вихідний! Можна спати довше.");
        } else {
            System.out.println("Робочий день. Підйом о 7:00!");
        }
        System.out.println();

        // ============================================================
        //   Оператор ! (НЕ / NOT) — інвертує значення
        // ============================================================
        System.out.println("=== Оператор ! (НЕ) ===");

        // Уявіть: двері зачинені. Якщо НЕ зачинені — можна зайти
        boolean isDoorLocked = false;


        System.out.println("Door is locked : " + isDoorLocked);
        System.out.println("Not operator " + !isDoorLocked);



        if (!isDoorLocked) {
            System.out.println("Двері відчинені — заходьте!");
        } else {
            System.out.println("Двері замкнені.");
        }
        // Результат: "Двері відчинені" (бо !false = true)

        System.out.println();

        // ============================================================
        //   Комбінування операторів
        // ============================================================
        System.out.println("=== Комбінування операторів ===");

        // Уявіть: квиток на літак зі знижкою
        // Знижка: вік < 12 АБО (вік >= 65 І є карта лояльності)
        int passengerAge = 70;
        boolean hasLoyaltyCard = true;

        if (passengerAge < 12 || (passengerAge >= 65 && hasLoyaltyCard)) {
            System.out.println("Знижка на квиток 30%!");
        } else {
            System.out.println("Повна вартість квитка.");
        }
        // Результат: "Знижка на квиток 30%!" (70 >= 65 І карта є)
    }
}
