package ua.com.javarush.jsquad.m1;

/**
 * Лекція 4: Умовний оператор if-else
 *
 * Умовний оператор — це як розвилка на дорозі:
 * ти дивишся на знак (умову) і вирішуєш, куди їхати.
 *
 * Аналогія з життя: Ви підходите до дверей магазину.
 * Якщо (if) двері відчинені — заходите.
 * Інакше (else) — йдете до іншого магазину.
 *
 * Синтаксис:
 *   if (умова) {
 *       команда1;    // виконується, якщо умова ІСТИННА (true)
 *   } else {
 *       команда2;    // виконується, якщо умова ХИБНА (false)
 *   }
 *
 * Реальне застосування: перевірка віку, пароля, балансу на карті тощо.
 */
public class Example01_IfElse {

    public static void main(String[] args) {

        // ============================================================
        //   Простий if-else: перевірка віку
        // ============================================================
        System.out.println("=== Перевірка віку ===");

        // Уявіть: ви охоронець на вході в клуб
        int age = 21;

        if (age < 18) {
            System.out.println("Ти ще дитина");
            System.out.println("Не сперечайся з дорослими");
        } else {
            System.out.println("Ви вже дорослий");
            System.out.println("Ну і молодь пішла");
        }
        // Результат: "Ти ще дитина" і "Не сперечайся з дорослими"
        // Тому що 17 < 18 — це true (істина)

        System.out.println();



















        // ============================================================
        //   Ще один приклад: перевірка погоди
        // ============================================================
        System.out.println("=== Перевірка погоди ===");

        // Уявіть: ви збираєтесь на прогулянку і дивитесь у вікно
        int temperature = 25;

        if (temperature > 20) {
            System.out.println("Тепло! Можна одягнути футболку");
        } else {
            System.out.println("Холодно! Одягни куртку");
        }
        // Результат: "Тепло! Можна одягнути футболку"
        // Тому що 25 > 20 — це true

        System.out.println();

        // ============================================================
        //   Приклад: парне чи непарне число
        // ============================================================
        System.out.println("=== Парне чи непарне? ===");

        // Реальне застосування: рядки в таблиці часто фарбують через один
        int number = 6;

        if (number % 2 == 0) {
            System.out.println(number + " — парне число");
        } else {
            System.out.println(number + " — непарне число");
        }
        // Результат: "7 — непарне число"
        // Тому що 7 % 2 = 1 (не дорівнює 0)
    }
}
