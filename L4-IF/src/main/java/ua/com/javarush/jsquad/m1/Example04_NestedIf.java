package ua.com.javarush.jsquad.m1;

import java.util.Scanner;

/**
 * Лекція 4: Вкладені if-и (послідовність if-else-if)
 *
 * Оператори if-else можна вкладати один в одного для складної логіки.
 *
 * Аналогія з життя: Ви збираєтесь на прогулянку і вирішуєте, що одягнути.
 * Дивитесь на термометр:
 *   - Вище за 20 градусів? -> сорочка
 *   - Від 10 до 20?        -> светр
 *   - Від 0 до 10?         -> плащ
 *   - Нижче за 0?          -> пальто
 * Це серія перевірок, де кожна наступна працює, якщо попередня не спрацювала.
 *
 * Реальне застосування: система оцінок, визначення пори року, тарифи.
 */
public class Example04_NestedIf {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Що одягнути? (вкладені if)
        // ============================================================


        Scanner a = new Scanner(System.in);

        String b = "asd";

        System.out.println(a.equals(b));

        System.out.println("=== Що одягнути? ===");

        int temperature = 9;

        if (temperature > 20) {
            System.out.println("Одягнути сорочку");
        } else {
            // Тут температура <= 20
            if (temperature > 10) {
                System.out.println("Одягнути светр");
            } else {
                // Тут температура <= 10
                if (temperature > 0) {
                    System.out.println("Одягнути плащ");
                } else {
                    System.out.println("Одягнути пальто");
                }
            }
        }
        // Результат: "Одягнути плащ" (бо 9 > 0, але 9 <= 10)

        System.out.println();

        // ============================================================
        //   Приклад 2: Система оцінок (if-else-if ланцюжок)
        // ============================================================
        System.out.println("=== Система оцінок ===");

        // Уявіть: вчитель перевіряє тест і ставить оцінку
        int score = 75;

        if (score >= 90) {
            System.out.println("Оцінка: Відмінно (A)");
        } else if (score >= 75) {
            System.out.println("Оцінка: Добре (B)");
        } else if (score >= 60) {
            System.out.println("Оцінка: Задовільно (C)");
        } else {
            System.out.println("Оцінка: Незадовільно (F)");
        }
        // Результат: "Оцінка: Добре (B)" (бо 75 >= 75)

        System.out.println();

        // ============================================================
        //   Приклад 3: Тарифи на таксі
        // ============================================================
        System.out.println("=== Тарифи на таксі ===");

        // Уявіть: додаток Uber рахує ціну залежно від відстані
        int distanceKm = 12;

        if (distanceKm <= 3) {
            System.out.println("Мінімальна поїздка: 50 грн");
        } else if (distanceKm <= 10) {
            System.out.println("Середня поїздка: " + (50 + (distanceKm - 3) * 8) + " грн");
        } else if (distanceKm <= 30) {
            System.out.println("Далека поїздка: " + (50 + 7 * 8 + (distanceKm - 10) * 6) + " грн");
        } else {
            System.out.println("Міжміська поїздка: договірна ціна");
        }
        // Результат: "Далека поїздка: 118 грн"
    }
}
