package ua.com.javarush.jsquad.m1;

import java.util.HashMap;

/**
 * Лекція 18: Колекції Map — Основи HashMap
 *
 * Map — це колекція, що зберігає пари «ключ — значення» (key-value).
 * Кожен ключ унікальний, а значення може повторюватись.
 *
 * Синтаксис:
 *   HashMap<ТипКлюча, ТипЗначення> map = new HashMap<>();
 *
 * Основні методи:
 *   put(key, value) — додати/оновити пару
 *   get(key)        — отримати значення за ключем
 *
 * Аналогія з життя: Map — це телефонна книга. Ім'я (ключ) → номер телефону
 * (значення). Двох однакових імен бути не може — якщо записати новий номер
 * для існуючого імені, старий номер замінюється.
 *
 * Реальне застосування: словники, кеші, конфігурації, маппінг ID → об'єкт,
 * підрахунок частоти елементів, маршрутизація URL → обробник.
 */
public class Example01_HashMapBasics {

    public static void main(String[] args) {

        // ============================================================
        //   Блок 1: Створення HashMap — телефонна книга
        // ============================================================
        System.out.println("=== Блок 1: Створення HashMap ===");

        // Телефонна книга: ім'я контакту → номер телефону
        HashMap<String, String> phoneBook = new HashMap<>();

        // put(key, value) — додаємо контакти
        phoneBook.put("Олена", "+380-67-111-22-33");
        phoneBook.put("Андрій", "+380-50-444-55-66");
        phoneBook.put("Марія", "+380-93-777-88-99");
        phoneBook.put("Дмитро", "+380-63-000-11-22");

        System.out.println("Телефонна книга: " + phoneBook);
        System.out.println("Кількість контактів: " + phoneBook.size());
        System.out.println();

        // ============================================================
        //   Блок 2: get() — пошук номера за ім'ям
        // ============================================================
        System.out.println("=== Блок 2: Отримання значення за ключем ===");

        // Уявіть: шукаєте номер друга в телефонній книзі
        String olenaPhone = phoneBook.get("Олена");
        System.out.println("Номер Олени: " + olenaPhone);

        String andriyPhone = phoneBook.get("Андрій");
        System.out.println("Номер Андрія: " + andriyPhone);

        // Якщо ключ не існує — повертає null
        String unknownPhone = phoneBook.get("Петро");
        System.out.println("Номер Петра: " + unknownPhone); // null — такого контакту немає
        System.out.println();

        // ============================================================
        //   Блок 3: put() замінює значення для існуючого ключа
        // ============================================================
        System.out.println("=== Блок 3: Оновлення значення ===");

        // Олена змінила номер — записуємо новий
        System.out.println("Старий номер Олени: " + phoneBook.get("Олена"));
        phoneBook.put("Олена", "+380-67-999-99-99");
        System.out.println("Новий номер Олени:  " + phoneBook.get("Олена"));
        System.out.println("Кількість контактів: " + phoneBook.size() + " (не змінилась!)");
        System.out.println();

        // ============================================================
        //   Блок 4: Ключі унікальні, значення — ні
        // ============================================================
        System.out.println("=== Блок 4: Унікальність ключів ===");

        // Два різних контакти можуть мати однаковий номер (спільний телефон)
        HashMap<String, String> familyPhones = new HashMap<>();
        familyPhones.put("Тато", "+380-67-100-00-00");
        familyPhones.put("Мама", "+380-67-100-00-00"); // той самий номер — ОК
        familyPhones.put("Син", "+380-50-200-00-00");

        System.out.println("Сімейні контакти: " + familyPhones);
        System.out.println("Тато і Мама мають однаковий номер — це допустимо.");
        System.out.println("Але два ключі 'Тато' — неможливо, другий put() замінить перший.");
    }
}
