package ua.com.javarush.jsquad.m1;

import java.util.Locale;

/**
 * Лекція 7: Масиви — Масив рядків (String[])
 *
 * Масиви можуть зберігати не тільки числа, а й рядки (String).
 * Значення за замовчуванням для String — це null (порожнє посилання),
 * а не порожній рядок "".
 *
 * Синтаксис:
 *   String[] масив = new String[розмір];
 *   String[] масив = {"значення1", "значення2", ...};
 *
 * Аналогія з життя: список покупок на аркуші паперу.
 * Кожен рядок у списку — один елемент масиву.
 * Якщо рядок порожній — там написано "null" (нічого).
 *
 * Реальне застосування: список імен, меню ресторану,
 * назви місяців, дні тижня.
 */
public class Example05_StringArray {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Масив рядків з днями тижня
        // ============================================================
        System.out.println("=== Дні тижня ===");

        String[] days = {"Понеділок", "Вівторок", "Середа", "Четвер",
                          "П'ятниця", "Субота", "Неділя"};

        for (int i = 0; i < days.length; i++) {
            System.out.println("День " + (i + 1) + ": " + days[i]);
        }
        System.out.println();

        // ============================================================
        //   Приклад 2: Значення за замовчуванням — null
        // ============================================================
        System.out.println("=== Значення за замовчуванням ===");

        // Уявіть: список гостей на вечірку — спочатку порожній
        String[] guests = new String[4];

        System.out.println("Гість 0: " + guests[0]); // null
        System.out.println("Гість 1: " + guests[1]); // null

        // Заповнюємо список
        guests[0] = "Андрій";
        guests[1] = "Марія";
        guests[2] = "Олексій";
        // guests[3] залишається null — хтось ще не підтвердив

        System.out.println();
        System.out.println("Після заповнення:");
        for (int i = 0; i < guests.length; i++) {
            System.out.println("Гість " + i + ": " + guests[i]);
        }
        System.out.println();

        // ============================================================
        //   Приклад 3: Українські місяці
        // ============================================================
        System.out.println("=== Місяці року ===");

        // Реальне застосування: перетворення номера місяця в назву
        String[] months = {"Січень", "Лютий", "Березень", "Квітень",
                            "Травень", "Червень", "Липень", "Серпень",
                            "Вересень", "Жовтень", "Листопад", "Грудень"};

        int monthNumber = 9; // вересень
        System.out.println("Місяць №" + monthNumber + " — " + months[monthNumber - 1]);
        // Зверніть увагу: місяць 9 має індекс 8 (бо індексація з 0)
    }
}
