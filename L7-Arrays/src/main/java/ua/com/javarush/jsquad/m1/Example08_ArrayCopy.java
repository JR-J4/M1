package ua.com.javarush.jsquad.m1;

import java.util.Arrays;

/**
 * Лекція 7: Масиви — Копіювання масивів
 *
 * Присвоєння масиву іншій змінній НЕ створює копію!
 * Обидві змінні вказують на один і той самий масив у пам'яті.
 * Для створення справжньої копії потрібно копіювати поелементно
 * або використовувати Arrays.copyOf().
 *
 * Синтаксис:
 *   int[] copy = Arrays.copyOf(оригінал, довжина);
 *
 * Аналогія з життя: два ключі від одного сейфу.
 * Якщо хтось поклав гроші через один ключ — другий теж бачить зміни.
 * Щоб мати незалежний сейф — потрібно купити другий і перенести речі.
 *
 * Реальне застосування: збереження початкового стану даних,
 * створення резервної копії перед сортуванням.
 */
public class Example08_ArrayCopy {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Пастка — присвоєння копіює посилання, не дані
        // ============================================================
        System.out.println("=== Пастка з посиланням ===");

        int[] original = {1, 2, 3, 4, 5};
        int[] notACopy = original; // це НЕ копія! Обидва вказують на один масив

        notACopy[0] = 999; // змінюємо через "копію"

        System.out.println("original[0] = " + original[0]);  // 999 — змінився!
        System.out.println("notACopy[0] = " + notACopy[0]);  // 999
        System.out.println("Обидві змінні вказують на один масив!");
        System.out.println();

        // ============================================================
        //   Приклад 2: Справжня копія через цикл
        // ============================================================
        System.out.println("=== Копіювання через цикл ===");

        int[] source = {10, 20, 30, 40, 50};
        int[] copy = new int[source.length]; // створюємо новий масив

        for (int i = 0; i < source.length; i++) {
            copy[i] = source[i]; // копіюємо кожен елемент
        }

        copy[0] = 999; // змінюємо копію

        System.out.println("source[0] = " + source[0]); // 10 — не змінився!
        System.out.println("copy[0]   = " + copy[0]);   // 999
        System.out.println("Тепер це незалежні масиви!");
        System.out.println();

        // ============================================================
        //   Приклад 3: Копіювання через Arrays.copyOf()
        // ============================================================
        System.out.println("=== Arrays.copyOf() ===");

        // Реальне застосування: зберегти оригінал перед сортуванням
        int[] grades = {8, 12, 5, 10, 7};
        int[] backup = Arrays.copyOf(grades, grades.length);

        // Тепер можна змінювати grades — backup збережений
        grades[0] = 0;

        System.out.println("grades: " + Arrays.toString(grades));   // [0, 12, 5, 10, 7]
        System.out.println("backup: " + Arrays.toString(backup));   // [8, 12, 5, 10, 7]
    }
}
