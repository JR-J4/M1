package ua.com.javarush.jsquad.m1;

/**
 * Лекція 7: Масиви — Доступ до елементів
 *
 * Кожен елемент масиву має свій індекс (номер).
 * Індексація починається з 0, а не з 1!
 * Останній елемент має індекс length - 1.
 *
 * Синтаксис:
 *   масив[індекс] = значення;   // запис
 *   тип змінна = масив[індекс]; // читання
 *
 * Аналогія з життя: номери кімнат у готелі.
 * Перша кімната на поверсі — №0 (а не №1, як ми звикли).
 * Щоб потрапити в кімнату, потрібно знати її номер.
 *
 * Реальне застосування: зберігання та отримання даних за позицією —
 * пікселі на екрані, товари в кошику, символи в рядку.
 */
public class Example02_ArrayElements {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Запис і читання елементів
        // ============================================================
        System.out.println("=== Запис і читання елементів ===");

        // Уявіть: ви заповнюєте поштові скриньки в під'їзді
        int[] mailboxes = new int[5];

        // Запис — кладемо листи в скриньки
        mailboxes[0] = 3;  // в скриньку 0 поклали 3 листи
        mailboxes[1] = 1;
        mailboxes[2] = 0;
        mailboxes[3] = 5;
        mailboxes[4] = 2;

        // Читання — перевіряємо скільки листів
        System.out.println("Скринька 0: " + mailboxes[0] + " листів"); // 3
        System.out.println("Скринька 3: " + mailboxes[3] + " листів"); // 5
        System.out.println();

        // ============================================================
        //   Приклад 2: Індекс починається з 0
        // ============================================================
        System.out.println("=== Індекс починається з 0 ===");

        // Реальне застосування: температура за кожен день тижня
        double[] temperature = new double[7];
        temperature[0] = 15.5; // Понеділок (перший день, індекс 0!)
        temperature[1] = 17.2; // Вівторок
        temperature[2] = 14.8; // Середа
        temperature[3] = 16.0; // Четвер
        temperature[4] = 18.3; // П'ятниця
        temperature[5] = 20.1; // Субота
        temperature[6] = 19.7; // Неділя

        System.out.println("Понеділок (індекс 0): " + temperature[0] + "°C");
        System.out.println("Неділя    (індекс 6): " + temperature[6] + "°C");
        System.out.println("Останній індекс = length - 1 = " + (temperature.length - 1));
        System.out.println();

        // ============================================================
        //   Приклад 3: Зміна значення елемента
        // ============================================================
        System.out.println("=== Зміна значення елемента ===");

        // Уявіть: ви виправляєте оцінку студента після перескладання
        int[] grades = new int[4];
        grades[0] = 5;
        grades[1] = 8;
        grades[2] = 3;  // незадовільна оцінка
        grades[3] = 10;

        System.out.println("До перескладання: оцінка[2] = " + grades[2]); // 3

        grades[2] = 7; // студент перескладав

        System.out.println("Після перескладання: оцінка[2] = " + grades[2]); // 7
    }
}
