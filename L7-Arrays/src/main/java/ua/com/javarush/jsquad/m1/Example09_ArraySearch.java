package ua.com.javarush.jsquad.m1;

import java.util.Arrays;

/**
 * Лекція 7: Масиви — Пошук елементів
 *
 * Щоб знайти елемент у масиві, потрібно перебрати всі елементи
 * і порівняти кожен із шуканим значенням. Це називається лінійний пошук.
 *
 * Синтаксис:
 *   for (int i = 0; i < масив.length; i++) {
 *       if (масив[i] == шукане) { ... }
 *   }
 *
 * Аналогія з життя: ви шукаєте потрібну книгу на полиці —
 * дивитесь на кожну по черзі, поки не знайдете потрібну.
 *
 * Реальне застосування: пошук товару за ціною, перевірка наявності
 * елемента, підрахунок кількості входжень.
 */
public class Example09_ArraySearch {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Пошук елемента за значенням
        // ============================================================
        System.out.println("=== Пошук елемента ===");

        // Уявіть: перевірка чи є ваш номер серед переможців лотереї
        int[] winningNumbers = {7, 14, 23, 35, 42, 49};
        int myNumber = 23;

        boolean found = false;

        for (int i = 0; i < winningNumbers.length; i++) {

            if (winningNumbers[i] == myNumber) {
                found = true;
                System.out.println("Номер " + myNumber + " знайдено на позиції " + i);
                break; // знайшли — далі шукати не потрібно
            }

        }
        if (!found) {
            System.out.println("Номер " + myNumber + " не знайдено");
        }
        System.out.println();

        // ============================================================
        //   Приклад 2: Підрахунок кількості входжень
        // ============================================================
        System.out.println("=== Підрахунок входжень ===");

        // Реальне застосування: скільки разів оцінка 10 зустрічається
        int[] grades = {10, 8, 10, 12, 10, 7, 9, 10};
        int target = 10;
        int count = 0;

        for (int i = 0; i < grades.length; i++) {
            if (grades[i] == target) {
                count++;
            }
        }
        System.out.println("Оцінка " + target + " зустрічається " + count + " разів"); // 4
        System.out.println();

        // ============================================================
        //   Приклад 3: Пошук рядка в масиві
        // ============================================================
        System.out.println("=== Пошук рядка ===");

        // Уявіть: перевірка чи є ваше ім'я у списку запрошених
        String[] invited = {"Андрій", "Марія", "Олена", "Богдан", "Вікторія"};
        String name = "Олена";

        boolean isInvited = false;

      for (String s : invited) {

        if (s.equals(name)) { // для String використовуємо equals()!
          isInvited = true;
          break;
        }

      }

        if (isInvited) {
            System.out.println(name + " є у списку запрошених!");
        } else {
            System.out.println(name + " немає у списку.");
        }
        System.out.println();

        // ============================================================
        //   Приклад 4: Пошук індексу мінімального елемента
        // ============================================================
        System.out.println("=== Індекс мінімуму ===");

        // Реальне застосування: знайти найдешевший товар
        int[] prices = {250, 180, 320, 95, 410, 150};
        int minIndex = 0;

        for (int i = 1; i < prices.length; i++) {
            if (prices[i] < prices[minIndex]) {
                minIndex = i;
            }
        }


        System.out.println("Найдешевший товар: " + prices[minIndex] + " грн (позиція " + minIndex + ")");
    }
}
