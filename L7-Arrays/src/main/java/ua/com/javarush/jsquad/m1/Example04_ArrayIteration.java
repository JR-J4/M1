package ua.com.javarush.jsquad.m1;

/**
 * Лекція 7: Масиви — Перебір елементів циклом
 *
 * Масиви найчастіше використовуються разом із циклами.
 * Цикл дозволяє обробити всі елементи масиву автоматично,
 * незалежно від його розміру.
 *
 * Синтаксис (for):
 *   for (int i = 0; i < масив.length; i++) {
 *       // масив[i] — поточний елемент
 *   }
 *
 * Аналогія з життя: вчитель перевіряє зошити всіх учнів по черзі.
 * Він бере зошит №0, потім №1, потім №2... і так до останнього.
 *
 * Реальне застосування: обчислення суми, пошук максимуму,
 * виведення всіх елементів на екран.
 */
public class Example04_ArrayIteration {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Перебір масиву циклом for
        // ============================================================
        System.out.println("=== Перебір циклом for ===");

        // Уявіть: касир сканує всі товари в кошику
        int[] prices = {45, 120, 30, 85, 200};


        System.out.print("Товари: ");

        for (int i = 0; i < prices.length; i++) {
            System.out.print( prices[i] + " грн  ");
        }

        System.out.println();
        System.out.println();

        // ============================================================
        //   Приклад 2: Сума елементів масиву
        // ============================================================
        System.out.println("=== Сума елементів ===");

//        int[] prices = {45, 120, 30, 85, 200};

        // Реальне застосування: підрахунок загальної вартості покупок
        int sum = 0;
        for (int i = 0; i < prices.length; i++) {
            sum = sum + prices[i];
        }
        System.out.println("Загальна вартість: " + sum + " грн"); // 480
        System.out.println();

        // ============================================================
        //   Приклад 3: Пошук максимального елемента
        // ============================================================
        System.out.println("=== Пошук максимуму ===");

        // Уявіть: визначаємо найвищу температуру за тиждень
        double[] temps = {15.5, 17.2, 14.8, 21.0, 18.3, 20.1, 19.7};

        double max = temps[0]; // припускаємо, що перший — найбільший

        for (int i = 1; i < temps.length; i++) {

            if (temps[i] > max) {
                max = temps[i];
            }

        }

        System.out.println("Найвища температура: " + max + "°C"); // 21.0
        System.out.println();

        // ============================================================
        //   Приклад 4: Пошук мінімального елемента
        // ============================================================
        System.out.println("=== Пошук мінімуму ===");

        double min = temps[0];
        for (int i = 1; i < temps.length; i++) {
            if (temps[i] < min) {
                min = temps[i];
            }
        }
        System.out.println("Найнижча температура: " + min + "°C"); // 14.8
        System.out.println();

        // ============================================================
        //   Приклад 5: Перебір масиву циклом while
        // ============================================================
        System.out.println("=== Перебір циклом while ===");

        int[] scores = {85, 92, 78, 95, 88};
        int i = 0;
        while (i < scores.length) {
            System.out.println("Студент " + (i + 1) + ": " + scores[i] + " балів");
            i++;
        }
    }
}
