package ua.com.javarush.jsquad.m1;

/**
 * Лекція 7: Масиви — Швидка ініціалізація
 *
 * Якщо ви заздалегідь знаєте значення елементів, можна створити
 * масив і одразу заповнити його — без вказування розміру.
 * Java сама порахує кількість елементів.
 *
 * Синтаксис:
 *   тип[] масив = {знач1, знач2, знач3};
 *   тип[] масив = new тип[] {знач1, знач2, знач3};
 *
 * Аналогія з життя: замість того, щоб купити порожню шафу і потім
 * розкладати речі по полицях — ви купуєте вже заповнену шафу.
 *
 * Реальне застосування: таблиця кількості днів у місяцях,
 * список фіксованих налаштувань, набір тестових даних.
 */
public class Example06_ArrayInitialization {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Швидка ініціалізація з фігурними дужками
        // ============================================================
        System.out.println("=== Швидка ініціалізація ===");

        // Оцінки студента за семестр
        int[] grades = {10, 8, 12, 9, 11, 7};

        System.out.println("Кількість оцінок: " + grades.length); // 6
        System.out.print("Оцінки: ");
        for (int i = 0; i < grades.length; i++) {
            System.out.print(grades[i] + " ");
        }
        System.out.println();
        System.out.println();

        // ============================================================
        //   Приклад 2: Ініціалізація з new тип[]
        // ============================================================
        System.out.println("=== Ініціалізація з new тип[] ===");

        // Цей спосіб потрібен, коли масив передається як аргумент
        int[] fibonacci = new int[]{1, 1, 2, 3, 5, 8, 13, 21};

        System.out.print("Числа Фібоначчі: ");
        for (int i = 0; i < fibonacci.length; i++) {
            System.out.print(fibonacci[i] + " ");
        }
        System.out.println();
        System.out.println();

        // ============================================================
        //   Приклад 3: Кількість днів у кожному місяці
        // ============================================================
        System.out.println("=== Дні в місяцях ===");

        // Реальне застосування: валідація дати
        int[] daysInMonth = {31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
        String[] monthNames = {"Січень", "Лютий", "Березень", "Квітень",
                                "Травень", "Червень", "Липень", "Серпень",
                                "Вересень", "Жовтень", "Листопад", "Грудень"};


        for (int i = 0; i < daysInMonth.length; i++) {
            System.out.println(monthNames[i] + ": " + daysInMonth[i] + " днів");
        }

        System.out.println();

        // ============================================================
        //   Приклад 4: Порівняння двох способів створення
        // ============================================================
        System.out.println("=== Порівняння способів ===");

        // Спосіб 1: створити порожній, потім заповнити
        int[] way1 = new int[3];
        way1[0] = 10;
        way1[1] = 20;
        way1[2] = 30;

        // Спосіб 2: швидка ініціалізація (коротше і зручніше)
        int[] way2 = {10, 20, 30};

        System.out.println("Спосіб 1, елемент 0: " + way1[0]); // 10
        System.out.println("Спосіб 2, елемент 0: " + way2[0]); // 10
        System.out.println("Обидва способи дають однаковий результат!");
    }
}
