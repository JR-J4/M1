package ua.com.javarush.jsquad.m1;

/**
 * Лекція 7: Масиви — Основи
 *
 * Масив — це структура даних, яка зберігає кілька значень одного типу.
 * Замість того, щоб створювати 100 окремих змінних, можна створити один масив.
 *
 * Синтаксис:
 *   тип[] ім'я;                  // оголошення
 *   ім'я = new тип[розмір];      // створення
 *   тип[] ім'я = new тип[розмір]; // оголошення + створення
 *
 * Аналогія з життя: масив — це ряд шкільних шафок з номерами.
 * Кожна шафка має свій номер (індекс) і може зберігати одну річ (значення).
 * Кількість шафок фіксована — не можна додати нові після встановлення.
 *
 * Реальне застосування: зберігання оцінок студентів, температур за тиждень,
 * списку цін товарів.
 */
public class Example01_ArrayBasics {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Навіщо потрібен масив?
        // ============================================================
        System.out.println("=== Навіщо потрібен масив? ===");

        // Без масиву — створюємо окремі змінні для кожної оцінки
        int grade1 = 10;
        int grade2 = 8;
        int grade3 = 12;
        int grade4 = 9;
        int grade5 = 11;
        System.out.println("Без масиву: " + grade1 + ", " + grade2 + ", " + grade3 + ", " + grade4 + ", " + grade5);

        // З масивом — всі оцінки в одному місці
        int[] grades = new int[5];
        grades[0] = 10;
        grades[1] = 8;
        grades[2] = 12;
        grades[3] = 9;
        grades[4] = 11;
        System.out.println("З масивом:  " + grades[0] + ", " + grades[1] + ", " + grades[2] + ", " + grades[3] + ", " + grades[4]);
        System.out.println();

        // ============================================================
        //   Приклад 2: Оголошення і створення масиву
        // ============================================================
        System.out.println("=== Оголошення і створення масиву ===");

        // Спосіб 1: окремо оголошення і створення
        int[] numbers;              // оголошення (ще немає масиву в пам'яті)
        numbers = new int[3];       // створення масиву на 3 елементи

        // Спосіб 2: оголошення і створення одночасно
        double[] prices = new double[5];

        System.out.println("Масив numbers створено на 3 елементи");
        System.out.println("Масив prices створено на 5 елементів");
        System.out.println();

        // ============================================================
        //   Приклад 3: Значення за замовчуванням
        // ============================================================
        System.out.println("=== Значення за замовчуванням ===");

        // Уявіть: нові шафки — вони порожні, але Java ставить "заглушки"
        int[] intArray = new int[3];
        System.out.println("int за замовчуванням:     " + intArray[0]); // 0

        double[] doubleArray = new double[3];
        System.out.println("double за замовчуванням:  " + doubleArray[0]); // 0.0

        boolean[] boolArray = new boolean[3];
        System.out.println("boolean за замовчуванням: " + boolArray[0]); // false

        String[] stringArray = new String[3];
        System.out.println("String за замовчуванням:  " + stringArray[0]); // null
        System.out.println();

        // ============================================================
        //   Приклад 4: Розмір масиву фіксований
        // ============================================================
        System.out.println("=== Розмір масиву фіксований ===");

        // Уявіть: ви купили полицю на 4 книги — більше не вмістити
        int[] shelf = new int[4];

        System.out.println("Розмір полиці: " + shelf.length); // 4
        // shelf = new int[10]; — можна створити НОВИЙ масив, але старий зникне
        System.out.println("Розмір масиву не можна змінити після створення!");
    }
}
