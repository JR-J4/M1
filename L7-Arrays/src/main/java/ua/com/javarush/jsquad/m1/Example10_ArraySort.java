package ua.com.javarush.jsquad.m1;

import java.util.Arrays;

/**
 * Лекція 7: Масиви — Сортування
 *
 * Сортування — це впорядкування елементів масиву (за зростанням або спаданням).
 * Java надає готовий метод Arrays.sort(), який сортує масив за зростанням.
 *
 * Синтаксис:
 *   Arrays.sort(масив);                          // весь масив
 *   Arrays.sort(масив, відІндексу, доІндексу);   // частина масиву
 *
 * Аналогія з життя: ви розкладаєте книги на полиці за алфавітом
 * або сортуєте купюри в гаманці за номіналом.
 *
 * Реальне застосування: впорядкування результатів пошуку,
 * рейтинг гравців, сортування цін від дешевих до дорогих.
 */
public class Example10_ArraySort {

    public static void main(String[] args) {


        // ============================================================
        //   Приклад 1: Сортування масиву чисел
        // ============================================================
        System.out.println("=== Сортування чисел ===");

        // Уявіть: рейтинг гравців за очками
        int[] scores = {85, 42, 97, 63, 78, 51};

        System.out.println("До сортування:    " + Arrays.toString(scores));
        Arrays.sort(scores);
        System.out.println("Після сортування: " + Arrays.toString(scores));
        // [42, 51, 63, 78, 85, 97]
        System.out.println();

        // ============================================================
        //   Приклад 2: Сортування рядків (за алфавітом)
        // ============================================================
        System.out.println("=== Сортування рядків ===");

        String[] fruits = {"Яблуко", "Банан", "Апельсин", "Вишня", "Груша"};

        System.out.println("До:    " + Arrays.toString(fruits));
        Arrays.sort(fruits);
        System.out.println("Після: " + Arrays.toString(fruits));
        System.out.println();

        // ============================================================
        //   Приклад 3: Сортування змінює оригінальний масив!
        // ============================================================
        System.out.println("=== Сортування змінює масив! ===");

        // Реальне застосування: зберегти оригінал перед сортуванням
        int[] original = {50, 10, 40, 20, 30};
        int[] backup = Arrays.copyOf(original, original.length); // зберігаємо копію

        Arrays.sort(original);

        System.out.println("Оригінал (відсортований): " + Arrays.toString(original));
        System.out.println("Резервна копія:           " + Arrays.toString(backup));
        System.out.println();

        // ============================================================
        //   Приклад 4: Мін і макс після сортування
        // ============================================================
        System.out.println("=== Мін і макс після сортування ===");

        // Після сортування: перший елемент — мінімум, останній — максимум
        int[] prices = {320, 150, 95, 410, 180, 250};
        Arrays.sort(prices);

        System.out.println("Ціни (відсортовані): " + Arrays.toString(prices));
        System.out.println("Найдешевший: " + prices[0] + " грн");
        System.out.println("Найдорожчий: " + prices[prices.length - 1] + " грн");
    }
}
