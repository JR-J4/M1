package ua.com.javarush.jsquad.m1;

/**
 * Лекція 7: Масиви — Властивість length
 *
 * Кожен масив має властивість length, яка повертає кількість елементів.
 * Це НЕ метод (без дужок): array.length, а не array.length().
 * Останній допустимий індекс завжди = length - 1.
 *
 * Синтаксис:
 *   int розмір = масив.length;
 *
 * Аналогія з життя: кількість полиць у шафі.
 * Якщо полиць 5, то вони пронумеровані 0, 1, 2, 3, 4 — а не до 5!
 *
 * Реальне застосування: перевірка розміру масиву перед доступом,
 * визначення останнього елемента, створення циклів.
 */
public class Example03_ArrayLength {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Отримання довжини масиву
        // ============================================================
        System.out.println("=== Довжина масиву ===");

        int[] numbers = {10, 20, 30, 40, 50};

        System.out.println("Кількість елементів: " + numbers.length);  // 5
        System.out.println("Перший елемент (індекс 0): " + numbers[0]);  // 10
        System.out.println("Останній елемент (індекс length-1): " + numbers[numbers.length - 1]); // 50
        System.out.println();

        // ============================================================
        //   Приклад 2: length vs останній індекс
        // ============================================================
        System.out.println("=== length vs останній індекс ===");

        // Уявіть: 5 учнів сидять за партами з номерами 0, 1, 2, 3, 4
        String[] students = {"Олена", "Богдан", "Вікторія", "Григорій", "Дарина"};

        System.out.println("Кількість учнів (length): " + students.length);        // 5
        System.out.println("Останній номер парти:      " + (students.length - 1));  // 4
        System.out.println("Останній учень: " + students[students.length - 1]);     // Дарина
        System.out.println();

        // ============================================================
        //   Приклад 3: Використання length в умовах
        // ============================================================
        System.out.println("=== Перевірка перед доступом ===");

        // Реальне застосування: безпечний доступ до елемента
        int[] data = {100, 200, 300};
        int index = 5;

        if (index < data.length) {
            System.out.println("Елемент: " + data[index]);
        } else {
            System.out.println("Індекс " + index + " поза межами масиву (розмір = " + data.length + ")");
        }
    }
}
