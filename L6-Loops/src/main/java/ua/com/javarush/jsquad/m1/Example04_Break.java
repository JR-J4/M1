package ua.com.javarush.jsquad.m1;

/**
 * Лекція 6: Команда break
 *
 * Якщо виконати команду break всерединi циклу,
 * вiн вiдразу завершується: програма почне виконувати
 * команди, якi йдуть ПIСЛЯ циклу.
 *
 * Аналогiя з життя: ви дивитесь серiал, але раптом
 * зателефонував бос — ви негайно вимикаєте телевiзор (break)
 * i йдете працювати (код пiсля циклу).
 *
 * Реальне застосування: пошук елемента, обробка помилок, вихiд з меню.
 */
public class Example04_Break {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Пошук першого дiльника
        // ============================================================
        System.out.println("=== Пошук першого дiльника ===");

        // Реальне застосування: перевiрка, чи є число простим
        int number = 91; // 91 = 7 * 13

        for (int i = 2; i < number; i++) {

            if (number % i == 0) {
                System.out.println("Перший дiльник " + number + " = " + i);
                break; // знайшли — далi шукати не потрiбно
            }

        }
        System.out.println();

        // ============================================================
        //   Приклад 2: Перевiрка пароля (максимум 3 спроби)
        // ============================================================
        System.out.println("=== Перевiрка пароля ===");

        // Реальне застосування: автентифiкацiя користувача
        String correctPin = "1234";
        String[] attempts = {"0000", "1234", "5678"}; // iмiтацiя вводу

        for (int i = 0; i < attempts.length; i++) {
            System.out.println("Спроба " + (i + 1) + ": введено " + attempts[i]);

            if (attempts[i].equals(correctPin)) {
                System.out.println("Пароль вiрний! Доступ дозволено.");
                break; // вийшли з циклу — далi перевiряти не потрiбно
            } else {
                System.out.println("Невiрний пароль!");
            }
        }
        System.out.println();

        // ============================================================
        //   Приклад 3: break у while (нескiнченний цикл з виходом)
        // ============================================================
        System.out.println("=== Нескiнченний цикл + break ===");

        // Реальне застосування: головний цикл гри або сервера
        int score = 0;

        while (true) {
            score += 10;
            System.out.println("Рахунок: " + score);

            if (score >= 50) {
                System.out.println("Гру завершено! Фiнальний рахунок: " + score);
                break;
            }
        }
    }
}
