        // Реальне застосування: повторення операцiї до успiшного результату
        int dice;
        int attempts = 0;

        do {
            dice = (int) (Math.random() * 6) + 1; // випадкове число 1-6
            attempts++;
            System.out.println("Спроба " + attempts + ": випало " + dice);
        } while (dice != 6);

        System.out.println("Ура! Шiстка випала з " + attempts + "-ї спроби!");
