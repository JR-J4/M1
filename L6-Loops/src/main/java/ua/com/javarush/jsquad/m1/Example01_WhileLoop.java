package ua.com.javarush.jsquad.m1;

/**
 * Лекція 6: Цикл while
 *
 * Цикл while складається з двох частин: умова і тіло циклу.
 * Команда або блок команд виконуються знову і знову,
 * поки умова циклу істинна (true).
 *
 * Синтаксис:
 *   while (умова) {
 *       блок команд;
 *   }
 *
 * Спочатку перевіряється умова, потім виконується тіло циклу.
 * Якщо умова відразу false — тіло не виконається жодного разу.
 *
 * Аналогія з життя: ви їсте, ПОКИ не наїстесь.
 * Кожна ложка — це одна ітерація (виток) циклу.
 *
 * Реальне застосування: очікування відповіді сервера, зчитування даних.
 */
public class Example01_WhileLoop {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Зворотний вiдлiк (як таймер мiкрохвильовки)
        // ============================================================
        System.out.println("=== Зворотний вiдлiк ===");

        int seconds = 5;
        while (seconds > 0) {
            System.out.println("Залишилось: " + seconds + " сек.");
            seconds--;
        }

        System.out.println("Час вийшов! Дзинь!");
        System.out.println();


        for (int seconds1 = 5; seconds1 > 0; seconds1--){
            System.out.println("Залишилось: " + seconds1 + " сек.");
        }




        // ============================================================
        //   Приклад 2: Сума чисел вiд 1 до 100
        // ============================================================
        System.out.println("=== Сума чисел вiд 1 до 100 ===");

        // Реальне застосування: підрахунок загальної вартості товарів
        int number = 1;
        int sum = 0;

        while (number <= 100) {
            sum = sum + number;
            number++;
        }
        System.out.println("Сума = " + sum); // 5050
        System.out.println();

        // ============================================================
        //   Приклад 3: Скiльки разiв число дiлиться на 2
        // ============================================================
        System.out.println("=== Ділення на 2 ===");

        // Реальне застосування: визначення кількості бітів у числі
        int value = 64;
        int count = 0;

        System.out.print(value);
        while (value > 1) {
            value = value / 2;
            count++;
            System.out.print(" -> " + value);
        }
        System.out.println();
        System.out.println("Кiлькiсть ділень: " + count); // 6
    }
}
