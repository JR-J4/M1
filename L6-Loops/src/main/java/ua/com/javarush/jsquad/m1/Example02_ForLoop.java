package ua.com.javarush.jsquad.m1;

/**
 * Лекція 6: Цикл for
 *
 * Цикл for — це ще один запис циклу while, просто компактнiший i зручнiший.
 * Усе в одному рядку: iнiцiалiзацiя, умова, крок.
 *
 * Синтаксис:
 *   for (команда1; умова; команда2) {
 *       блок команд;
 *   }
 *
 *   команда1 — виконується один раз перед циклом (зазвичай створення змiнної)
 *   умова    — перевiряється перед кожним витком
 *   команда2 — виконується пiсля кожного витка (зазвичай i++)
 *
 * Аналогiя з життя: "пробiжи 10 кiл навколо стадiону" —
 * ви знаєте заздалегiдь, скiльки разiв повторювати.
 *
 * Реальне застосування: обхiд списку товарiв, генерацiя таблиць.
 */
public class Example02_ForLoop {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Виведення чисел вiд 1 до 10
        // ============================================================
        System.out.println("=== Числа вiд 1 до 10 ===");

        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        System.out.println();



        // ============================================================
        //   Приклад 2: Таблиця множення на 7
        // ============================================================
        System.out.println("=== Таблиця множення на 7 ===");

        // Реальне застосування: генерацiя таблиць для звiтiв
        for (int i = 1; i <= 10; i++) {
            System.out.println("7 x " + i + " = " + (7 * i));
        }
        System.out.println();




        // ============================================================
        //   Приклад 3: Зворотний цикл (вiдлiк назад)
        // ============================================================
        System.out.println("=== Запуск ракети ===");

        // Реальне застосування: зворотний таймер
        for (int i = 10; i >= 1; i--) {
            System.out.println(i + "...");
        }
        System.out.println("ПУСК!");
        System.out.println();




        // ============================================================
        //   Приклад 4: Цикл з кроком 2 (парнi числа)
        // ============================================================
        System.out.println("=== Парнi числа вiд 0 до 20 ===");

        // Реальне застосування: обробка кожного другого елемента
        for (int i = 0, j = 2; i <= 20 && j < 6; i += 2, j++) {
            System.out.println(" i = " + i);
            System.out.println(" j = " + j);
        }
        System.out.println();
    }
}
