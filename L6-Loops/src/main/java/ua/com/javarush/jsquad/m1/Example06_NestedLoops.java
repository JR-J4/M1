package ua.com.javarush.jsquad.m1;

/**
 * Лекція 6: Цикл у циклi (вкладенi цикли)
 *
 * Щоб написати цикл у циклi, потрiбно в тiлi першого
 * (зовнiшнього) циклу написати другий (внутрiшнiй).
 *
 * Як це працює:
 *   - Зовнiшнiй цикл робить 1 виток
 *   - Внутрiшнiй цикл вiдпрацьовує ВСI свої витки
 *   - Зовнiшнiй цикл робить наступний виток
 *   - Внутрiшнiй знову вiдпрацьовує всi витки... i так далi.
 *
 * Аналогiя з життя: ви маєте 3 валiзи, у кожнiй 5 речей.
 * Зовнiшнiй цикл — перебирає валiзи. Внутрiшнiй — речi у валiзi.
 *
 * Реальне застосування: обробка таблиць, матриць, двовимiрних масивiв.
 */
public class Example06_NestedLoops {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Таблиця множення
        // ============================================================
        System.out.println("=== Таблиця множення 1-5 ===");

        // Реальне застосування: генерацiя таблиць
        for (int row = 1; row <= 5; row++) {

            for (int col = 1; col <= 5; col++) {
                // \t — це символ табуляцiї, щоб колонки були рiвнi
                System.out.print(row * col + "\t");
            }

            System.out.println(); // перехiд на новий рядок пiсля кожного рядка таблицi
        }

        System.out.println();

        // ============================================================
        //   Приклад 2: Прямокутник з зiрочок
        // ============================================================
        System.out.println("=== Прямокутник 4x8 ===");

        int rows = 4;
        int cols = 8;

        for (int r = 0; r < rows; r++) {
            for (int c = 0; c < cols; c++) {
                System.out.print("* ");
            }
            System.out.println();
        }
        System.out.println();

        // ============================================================
        //   Приклад 3: Трикутник (рядок i = i зiрочок)
        // ============================================================
        System.out.println("=== Трикутник ===");

        // Реальне застосування: вiзуалiзацiя рейтингу зiрочками
        for (int i = 1; i <= 5; i++) {
            for (int j = 0; j < i; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
        System.out.println();

        // ============================================================
        //   Приклад 4: Розклад (години x хвилини)
        // ============================================================
        System.out.println("=== Розклад будильникiв ===");

        // Реальне застосування: генерацiя часових слотiв
        for (int hour = 8; hour <= 10; hour++) {
            for (int min = 0; min < 60; min += 30) {
                System.out.printf("  %02d:%02d%n", hour, min);
            }
        }
    }
}
