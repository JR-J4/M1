package ua.com.javarush.jsquad.m1;

/**
 * Лекція 3: Як змінні зберігаються в оперативній пам'яті
 *
 * Кожна програма перед виконанням завантажується в оперативну пам'ять.
 * Пам'ять складається з маленьких комірок — байтів.
 * У кожної комірки є унікальний номер (адреса): 0, 1, 2, 3, ...
 *
 * Примітивні типи (int, double, boolean...) зберігають ЗНАЧЕННЯ прямо в комірках.
 *
 * String — це об'єкт. Змінна String зберігає не сам текст,
 * а АДРЕСУ (посилання) на об'єкт у пам'яті, де лежить текст.
 *
 * Аналогія:
 *   int — це книга на полиці (значення прямо тут)
 *   String — це записка з адресою бібліотеки (посилання на об'єкт)
 */
public class Example05_MemoryStorage {

    public static void main(String[] args) {

        // ============================================================
        //   Примітивні типи: значення зберігається прямо у змінній
        // ============================================================
        System.out.println("=== Примітивні типи (значення у змінній) ===");

        // Уявіть: кожна змінна — це коробка певного розміру
        byte b = 42;       // коробка на 1 байт, всередині лежить 42
        short s = 1000;    // коробка на 2 байти
        int i = 100500;    // коробка на 4 байти
        long l = 999999L;  // коробка на 8 байтів
        double d = 13.001; // коробка на 8 байтів

        System.out.println("byte b = " + b + "   (займає 1 байт у пам'яті)");
        System.out.println("short s = " + s + " (займає 2 байти)");
        System.out.println("int i = " + i + " (займає 4 байти)");
        System.out.println("long l = " + l + " (займає 8 байтів)");
        System.out.println("double d = " + d + " (займає 8 байтів)");
        System.out.println();

        // Коли копіюємо примітив — копіюється ЗНАЧЕННЯ
        int original = 10;
        int copy = original;  // copy отримує КОПІЮ значення 10

        copy = 99;  // змінюємо copy — original НЕ змінюється!

        System.out.println("=== Копіювання примітивів ===");
        System.out.println("original = " + original);  // 10 (не змінився!)
        System.out.println("copy = " + copy);           // 99
        System.out.println();

        // ============================================================
        //   String: змінна зберігає ПОСИЛАННЯ на об'єкт
        // ============================================================
        System.out.println("=== String (посилання на об'єкт) ===");

        // Змінна str не містить текст "Привіт" безпосередньо.
        // Вона містить АДРЕСУ комірки пам'яті, де зберігається об'єкт "Привіт"
        String str = "Привіт";
        String str2 = str;
        str = "Hello World!";

        System.out.println(str);
        System.out.println(str2);
        System.out.println("-----------------");

        // Як поштова адреса: змінна str = "вул. Пам'яті, буд. 13"
        // А сам текст "Привіт" знаходиться за цією адресою
        System.out.println("str = " + str);
        System.out.println();

        // ============================================================
        //   Розмір кожного типу в пам'яті
        // ============================================================
        System.out.println("=== Розмір типів у пам'яті ===");
        System.out.println("boolean: 1 біт   (true/false)");
        System.out.println("byte:    1 байт  (8 біт)");
        System.out.println("short:   2 байти (16 біт)");
        System.out.println("char:    2 байти (16 біт)");
        System.out.println("int:     4 байти (32 біти)");
        System.out.println("float:   4 байти (32 біти)");
        System.out.println("long:    8 байтів (64 біти)");
        System.out.println("double:  8 байтів (64 біти)");
        System.out.println();

        // ============================================================
        //   Значення за замовчуванням (для полів класу, не локальних!)
        // ============================================================
        System.out.println("=== Значення за замовчуванням (для полів класу) ===");
        System.out.println("byte, short, int, long -> 0");
        System.out.println("float, double          -> 0.0");
        System.out.println("boolean                -> false");
        System.out.println("char                   -> '\\u0000' (порожній символ)");
        System.out.println("String (та інші об'єкти) -> null");
    }
}
