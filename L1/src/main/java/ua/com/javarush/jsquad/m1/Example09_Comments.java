package ua.com.javarush.jsquad.m1;

/**
 * Лекція 1-2: Коментарі в Java
 *
 * Коментарі - це нотатки для програміста. Java їх повністю ігнорує.
 * Як стікери на полях підручника - для себе, а не для комп'ютера.
 *
 * Три способи написати коментар:
 *   1. // однорядковий - від // до кінця рядка
 *   2. Багаторядковий - від /* до * /  (без пробілу)
 *   3. Документація - від /** до * /   (для опису класів/методів)
 */
public class Example09_Comments {

    public static void main(String[] args) {


        // Це однорядковий коментар - Java його не бачить
        // Використовуй для коротких пояснень


        System.out.println("Ця команда виконається"); // коментар може бути в кінці рядка

//         System.out.println("А ця НЕ виконається - вона закоментована!");

        /*
         Це багаторядковий коментар.
         Зручно, коли треба написати довге пояснення
         або тимчасово "вимкнути" кілька рядків коду.
         */

        /* Можна і в один рядок */

        System.out.println("Коментарі не впливають на програму");
        System.out.println("Java виконує тільки код, не коментарі");



        // Лайфхак: коментарі допомагають зрозуміти код через місяць,
        // коли ви вже забули що і навіщо писали :)
    }
}
