package ua.com.javarush.jsquad.m1;

/**
 * Лекція 1-2: Перша програма на Java
 * <p>
 * Кожна програма на Java починається з класу (class) та методу main.
 * Уявіть клас як "коробку" для вашого коду, а main - як "вхідні двері" програми.
 * Коли ви натискаєте "Run" - Java шукає саме метод main і починає виконувати код з нього.
 */
public class Example01_HelloWorld {

  // main - точка входу в програму. Без нього програма не запуститься.
  // Це як кнопка "Старт" у грі.
  public static void main(String[] args) {

    // println - виводить текст на екран І переходить на новий рядок
    // "println" = "print line" = "надрукувати рядок"
    System.out.println("Привіт, світе!");


    System.out.println("Я вивчаю Java!");
    System.out.println("Це моя перша програма!");

    // Результат:
    // Привіт, світе!
    // Я вивчаю Java!
    // Це моя перша програма!
  }
}
