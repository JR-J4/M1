package ua.com.javarush.jsquad.m1;

/**
 * Лекція 8: Двовимірні масиви — Основи
 *
 * Двовимірний масив — це масив, елементами якого є інші масиви (масив масивів).
 * Він схожий на таблицю з рядками та стовпцями.
 *
 * Синтаксис:
 *   тип[][] ім'я = new тип[рядки][стовпці];
 *   ім'я[рядок][стовпець] = значення;
 *
 * Аналогія з життя: двовимірний масив — це таблиця Excel.
 * Кожна комірка має координати: номер рядка і номер стовпця.
 * Щоб знайти дані, потрібно знати обидва номери.
 *
 * Реальне застосування: розклад уроків (рядки — дні, стовпці — уроки),
 * таблиця оцінок студентів, карта гри, шахівниця.
 */
public class Example01_TwoDimensionalBasics {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Навіщо потрібен двовимірний масив?
        // ============================================================
        System.out.println("=== Навіщо потрібен двовимірний масив? ===");

        // Уявіть: у нас є оцінки 3-х студентів за 4 предмети
        // Без двовимірного масиву — купа окремих масивів:
        int[] student1 = {10, 8, 12, 9};
        int[] student2 = {7, 11, 10, 8};
        int[] student3 = {12, 9, 11, 10};
        System.out.println("Окремі масиви — незручно, якщо студентів 100!");

        // З двовимірним масивом — одна таблиця:
        int[][] grades = {
                student1,   // студент 0
                {7, 11, 10, 8},   // студент 1
                {12, 9, 11, 10}   // студент 2
        };
        System.out.println("Двовимірний масив — усі оцінки в одній таблиці!");
        System.out.println("Оцінка студента 1 за предмет 2: " + grades[1][2]); // 10
        System.out.println();

        // ============================================================
        //   Приклад 2: Створення двовимірного масиву
        // ============================================================
        System.out.println("=== Створення двовимірного масиву ===");

        // Сценарій: створюємо таблицю місць у кінотеатрі (3 ряди по 5 місць)
        int[][] cinema = new int[3][5];

        System.out.println("Кінотеатр: 3 ряди по 5 місць");
        System.out.println("Кількість рядів: " + cinema.length);       // 3
        System.out.println("Місць у ряді:    " + cinema[0].length);    // 5
        System.out.println();

        // ============================================================
        //   Приклад 3: Заповнення і зчитування елементів
        // ============================================================
        System.out.println("=== Заповнення і зчитування елементів ===");

        // Сценарій: бронюємо місця в кінотеатрі (1 — зайнято, 0 — вільне)
        cinema[0][2] = 1; // ряд 0, місце 2
        cinema[1][0] = 1; // ряд 1, місце 0
        cinema[1][1] = 1; // ряд 1, місце 1
        cinema[2][4] = 1; // ряд 2, місце 4

        System.out.println("Місце [0][2] зайнято? " + (cinema[0][2] == 1 ? "Так" : "Ні"));
        System.out.println("Місце [0][0] зайнято? " + (cinema[0][0] == 1 ? "Так" : "Ні"));
        System.out.println();

        // ============================================================
        //   Приклад 4: Значення за замовчуванням
        // ============================================================
        System.out.println("=== Значення за замовчуванням ===");

        // Як і в одновимірних масивах, елементи заповнюються за замовчуванням
        int[][] intTable = new int[2][3];
        double[][] doubleTable = new double[2][3];
        boolean[][] boolTable = new boolean[2][3];
        String[][] stringTable = new String[2][3];

        System.out.println("int:     " + intTable[0][0]);     // 0
        System.out.println("double:  " + doubleTable[0][0]);  // 0.0
        System.out.println("boolean: " + boolTable[0][0]);    // false
        System.out.println("String:  " + stringTable[0][0]);  // null
    }
}
