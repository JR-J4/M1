package ua.com.javarush.jsquad.m1;

/**
 * Лекція 8: Двовимірні масиви — Ініціалізація
 *
 * Двовимірний масив можна ініціалізувати кількома способами:
 * 1) new тип[рядки][стовпці] — створення порожнього масиву
 * 2) { {}, {}, {} } — швидка ініціалізація з конкретними значеннями
 * 3) Поелементне заповнення через цикли
 *
 * Аналогія з життя: заповнення таблиці результатів змагань.
 * Можна створити порожню таблицю і записувати результати по ходу гри,
 * або одразу підготувати заповнену таблицю.
 *
 * Реальне застосування: ініціалізація ігрового поля, таблиці множення,
 * календаря, матриці відстаней між містами.
 */
public class Example02_TwoDimensionalInit {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Швидка ініціалізація (літеральна)
        // ============================================================
        System.out.println("=== Швидка ініціалізація ===");

        // Сценарій: кількість днів у місяцях, згрупованих поквартально
        int[][] months = {
                {31, 28, 31},  // Q1: січень, лютий, березень
                {30, 31, 30},  // Q2: квітень, травень, червень
                {31, 31, 30},  // Q3: липень, серпень, вересень
                {31, 30, 31}   // Q4: жовтень, листопад, грудень
        };


        int[][][] months2 = {
                { {1, 2,3}, {1, 2,3} },  // Q1: січень, лютий, березень
                { {1, 2,3}, {1, 2,3} },  // Q1: січень, лютий, березень
        };

        System.out.println("Днів у лютому (Q1, місяць 1): " + months[0][1]); // 28
        System.out.println("Днів у серпні (Q3, місяць 1): " + months[2][1]); // 31
        System.out.println();

        // ============================================================
        //   Приклад 2: Створення + заповнення через цикли
        // ============================================================
        System.out.println("=== Таблиця множення (фрагмент) ===");

        // Сценарій: генеруємо таблицю множення 5x5
        int[][] multiplication = new int[5][5];

        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                multiplication[i][j] = (i + 1) * (j + 1);
            }
        }

        // Виводимо таблицю
        System.out.print("    ");
        for (int j = 1; j <= 5; j++) {
            System.out.printf("%4d", j);
        }
        System.out.println();
        System.out.println("    --------------------");

        for (int i = 0; i < 5; i++) {
            System.out.printf("%2d |", (i + 1));
            for (int j = 0; j < 5; j++) {
                System.out.printf("%4d", multiplication[i][j]);
            }
            System.out.println();
        }
        System.out.println();

        // ============================================================
        //   Приклад 3: Як це зберігається в пам'яті
        // ============================================================
        System.out.println("=== Як двовимірний масив зберігається в пам'яті ===");

        // Двовимірний масив — це насправді масив посилань на одновимірні масиви
        int[][] data = new int[3][4];

        // data — це масив з 3 елементів
        // Кожен елемент (data[0], data[1], data[2]) — це окремий масив з 4 елементів
        System.out.println("data.length = " + data.length);       // 3 (кількість рядків)
        System.out.println("data[0].length = " + data[0].length); // 4 (кількість стовпців у рядку 0)
        System.out.println("data[1].length = " + data[1].length); // 4
        System.out.println("data[2].length = " + data[2].length); // 4

        // У пам'яті комп'ютера немає "таблиць" — це просто 12 комірок поспіль.
        // Поділ на рядки і стовпці — лише наша абстракція.
        System.out.println("У пам'яті це просто " + (data.length * data[0].length) + " комірок!");
    }
}
