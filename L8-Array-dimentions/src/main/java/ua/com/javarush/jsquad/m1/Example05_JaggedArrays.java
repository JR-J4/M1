package ua.com.javarush.jsquad.m1;

/**
 * Лекція 8: Зубчасті масиви (Jagged Arrays)
 *
 * Зубчастий масив — це двовимірний масив, де кожен рядок може мати
 * різну кількість елементів (різну довжину).
 *
 * Синтаксис:
 *   тип[][] ім'я = new тип[кількість_рядків][];
 *   ім'я[0] = new тип[довжина_рядка_0];
 *   ім'я[1] = new тип[довжина_рядка_1];
 *   ...
 *
 * Аналогія з життя: зубчастий масив — це трибуна стадіону,
 * де кожен ряд має різну кількість місць (нижні ряди коротші, верхні — довші).
 *
 * Реальне застосування: зберігання даних різної довжини —
 * список учнів у класах різного розміру, коментарі до постів, рейси літаків.
 */
public class Example05_JaggedArrays {

    public static void main(String[] args) {

        // ============================================================
        //   Приклад 1: Створення зубчастого масиву
        // ============================================================
        System.out.println("=== Створення зубчастого масиву ===");

        // Сценарій: розклад занять на тиждень (різна кількість уроків щодня)
        // Спочатку створюємо "контейнер" на 5 днів, без вказівки довжини рядків
        String[][] schedule = new String[5][];

        // Кожен день має свою кількість уроків
        schedule[0] = new String[]{"Математика", "Фізика", "Історія", "Англійська"};
        schedule[1] = new String[]{"Хімія", "Біологія", "Фізика"};
        schedule[2] = new String[]{"Математика", "Географія", "Інформатика", "Музика", "Фізкультура"};
        schedule[3] = new String[]{"Історія", "Англійська"};
        schedule[4] = new String[]{"Математика", "Хімія", "Біологія"};

        String[] days = {"Пн", "Вт", "Ср", "Чт", "Пт"};

        for (int i = 0; i < schedule.length; i++) {
            System.out.print(days[i] + " (" + schedule[i].length + " уроків): ");

            for (int j = 0; j < schedule[i].length; j++) {
                System.out.print(schedule[i][j]);

                if (j < schedule[i].length - 1) {
                    System.out.print(", ");
                }

            }

            System.out.println();
        }
        System.out.println();

        // ============================================================
        //   Приклад 2: Зубчастий масив чисел (з лекції)
        // ============================================================
        System.out.println("=== Зубчастий масив чисел ===");

        int[][] matrix = new int[3][];
        matrix[0] = new int[]{1, 2, 3, 4, 5, 6};
        matrix[1] = new int[]{1, 2, 3};
        matrix[2] = new int[]{1, 3, 7, 9};

        // Зовнішній цикл — по рядках, внутрішній — по елементах рядка
        for (int i = 0; i < matrix.length; i++) {
            System.out.print("Рядок " + i + " (" + matrix[i].length + " ел.): ");
            for (int j = 0; j < matrix[i].length; j++) {
                System.out.print(matrix[i][j] + " ");
            }
            System.out.println();
        }
        System.out.println();

        // ============================================================
        //   Приклад 3: Трикутник Паскаля
        // ============================================================
        System.out.println("=== Трикутник Паскаля (зубчастий масив) ===");

        // Класичний приклад зубчастого масиву — кожен рядок довший на 1
        int rows = 7;
        int[][] pascal = new int[rows][];

        for (int i = 0; i < rows; i++) {
            pascal[i] = new int[i + 1];     // рядок i має (i+1) елементів
            pascal[i][0] = 1;               // перший елемент = 1
            pascal[i][i] = 1;               // останній елемент = 1

            for (int j = 1; j < i; j++) {
                pascal[i][j] = pascal[i - 1][j - 1] + pascal[i - 1][j];
            }
        }

        // Виведення трикутника
        for (int i = 0; i < rows; i++) {
            // Відступ для вирівнювання
            for (int s = 0; s < rows - i - 1; s++) {
                System.out.print("   ");
            }
            for (int j = 0; j < pascal[i].length; j++) {
                System.out.printf("%5d ", pascal[i][j]);
            }
            System.out.println();
        }
    }
}
